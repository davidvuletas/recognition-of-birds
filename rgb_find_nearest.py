    @staticmethod
    def rgbFromStr(s):
        # s starts with a #.
        r, g, b = int(s[1:3], 16), int(s[3:5], 16), int(s[5:7], 16)
        return r, g, b


    @staticmethod
    def findNearestWebColorName(points):
        return ColorNames.findNearestColorName(points, ColorNames.Color)


    @staticmethod
    def findNearestImageMagickColorName(points):
        return ColorNames.findNearestColorName(points, ColorNames.ImageMagickColorMap)


    @staticmethod
    def findNearestColorName(points, Map):
        (R, G, B) = points
        mindiff = None
        for d in Map:
            r, g, b = ColorNames.rgbFromStr(Map[d])
            diff = (R - r) * (R - r) + (G - g) * (G - g) + (B - b) * (B - b)
            if mindiff is None or diff < mindiff:
                mindiff = diff
                mincolorname = d
        return mincolorname
